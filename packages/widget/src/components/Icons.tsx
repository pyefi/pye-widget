export function WalletDot({ name, size = 32 }: { name: string; size?: number }) {
  const logos: Record<string, string> = { Phantom: phantomLogo, Backpack: backpackLogo, Metamask: metamaskLogo };
  const bg: Record<string, string> = { Phantom: "#AB9FF2", Backpack: "#E33B3B", Metamask: "#F5841F" };
  const logo = logos[name];
  if (logo) {
    return (
      <img src={logo} alt={name} style={{
        width: size, height: size, borderRadius: 8, flexShrink: 0,
        objectFit: "cover",
        boxShadow: "0px 4px 8px 0px rgba(0,0,0,0.07)",
      }} />
    );
  }
  return (
    <div style={{
      width: size, height: size, borderRadius: 8, flexShrink: 0,
      background: bg[name] || "#ccc",
      display: "flex", alignItems: "center", justifyContent: "center",
    }}>
      <span style={font(11, "#fff", 700)}>{name[0]}</span>
    </div>
  );
}
